public class Prog19_For_Loop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++){
            System.out.println(i);
        }

        String name = "OmkarSalunkeIsTheGreatestCoder";
        for (int i = 0; i < name.length(); i++) {
            System.out.print(name.charAt(i) + " ");
        }
    }
}
